				<p class="indent">An izakaya is a type of informal Japanese gastropub. They are casual places for after-work drinking. They have been compared to Irish pubs, tapas bars and early American saloons and taverns. However,Izakaya are a little different from bars in that diners are always seated and there is little opportunity for interaction with other customers. While drinking is a big part of it, there’s also a constant stream of (shared) dishes. It’s hard to classify the food other than “generally goes well with alcohol”. In fact the menu can be so diverse that it can be a chance to try some dishes you haven’t tried before. Typical izakaya dishes include chicken karaage (Japanese-style fried chicken), yakitori, edamame, grilled fish, french fries, sashimi, sushi, ebi-mayo (fried shrimp with mayo), nabe (Japanese hotpot) and salads. Izakaya are also typically attended by large groups of friends or colleagues and are rarely a dating spot.<p>				
